export const getTranslation = (language: string, key: keyof TranslationKeys): string => {
  const lang = language.toLowerCase();
  const translation = translations[lang] || translations['en'];
  return translation[key] || translations['en'][key];
};

export const formatDateLocalized = (date: string | Date, language: string): string => {
  const d = new Date(date);
  const months = [
    getTranslation(language, 'jan'),
    getTranslation(language, 'feb'),
    getTranslation(language, 'mar'),
    getTranslation(language, 'apr'),
    getTranslation(language, 'may'),
    getTranslation(language, 'jun'),
    getTranslation(language, 'jul'),
    getTranslation(language, 'aug'),
    getTranslation(language, 'sep'),
    getTranslation(language, 'oct'),
    getTranslation(language, 'nov'),
    getTranslation(language, 'dec')
  ];
  const month = months[d.getMonth()];
  const day = d.getDate();
  const year = d.getFullYear();
  return `${month} ${day}, ${year}`;
};
